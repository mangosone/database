  `type` INT(10) UNSIGNED NOT NULL DEFAULT '0' COMMENT 'Type of link',
  `effect_mask` INT(10) UNSIGNED NOT NULL DEFAULT '0' COMMENT 'mask of effect (NY)',
  `comment` VARCHAR(255) NOT NULL DEFAULT '',
